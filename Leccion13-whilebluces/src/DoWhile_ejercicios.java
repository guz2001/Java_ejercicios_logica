public class DoWhile_ejercicios {
    public static void main(String[] args){
        // ejemplo dowhile

        //contador de --
        int contar=1;
        do {
            System.out.println("era:"+contar);
            contar++;
            System.out.println("es:"+contar);
        }while (contar>1 && contar<10);


    }
}
