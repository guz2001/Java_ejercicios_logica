package tipos_datos_enteros;



public class tipos_datos_enteros{
    public static void main(String[] args) {
        //VARIABLES ENTERAS
        //byte solo almacena 8 byte en memoria
        byte bite = 2;
        System.out.println(bite);
        //ENTERO SHORT HASTA 16 BYTE
        Short corto= 10;
        System.out.println("este tipo de entero es: "+corto);
        //entero HASTA 2 MIL MILLONES O 32 BYTE
        int entero;
        entero = 16;
        System.out.println("NUMERO ENTERO ES: "+ entero);
        //entero long de 64 bytes
        long largo_entero= 123901290;// no es con decimales
        System.out.println(largo_entero);

    }

}
