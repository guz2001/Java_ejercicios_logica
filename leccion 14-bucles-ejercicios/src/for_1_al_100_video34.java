public class for_1_al_100_video34 {
    public static void main(String[] args){
        /*
        Escribir todos los numeros del 100 a 0 en 7
         */

        for(int i=100;i>=0;i-=7){// para i que empieza en 100, mientras i sea mayor o igual a 0, hacer que i reste 7 cada vez

            System.out.println("numeros bajando=> "+ i);

        }
    }
}
